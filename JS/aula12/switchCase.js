let escolha = 'A'

switch(escolha){
    case 'A':
        console.log('A opção escolhida foi A')
        break
    case 'B':
        console.log('A opção escolhida foi B')
        break
    case 'C':
        console.log('A opção escolhida foi C')
        break
    default:
        console.log('Opção inválida')
}

