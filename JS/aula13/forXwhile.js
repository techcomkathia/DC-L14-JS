//while
//contador/ variável de controle

let contador = 0
while (contador <= 10) {
    console.log(contador)
    //atualizar a variável de controle
    contador++
}

//for: ganho com relação a simplicidade da sintaxe
// declara a variável de controle ; condição de parada ; atualização
for (let i = 0; i <= 10; i++) {
    console.log(i)
}


//DICA:todas as vezes que você tiver controle de quantas vezes o laço deve ser executado, use o for
//quando você não tiver controle de quantas vezes o laço deve ser executado, use o while
