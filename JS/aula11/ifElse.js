// if else é utilizado quando temos duas possibilidades, sendo que quando uma não é verdade a outra é obrigatoriamente verdade

// impar / par
// positivo / negativo
// aprovado / reprovado
// verdadeiro / falso
let num1 = 40
let num2 = "40"

if(num1 > num2){ //false
    console.log("o número 1 é maior que o número 2")
}
else{
 //caso condição falsa esse código será executado
    console.log("o número 1 não é maior que o número 2")
}

if(num1 != num2){ 
    console.log("os números não são iguais")
}
else{
    console.log("os números são iguais")
}